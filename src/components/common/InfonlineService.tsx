export function trigInfonline(section: string, value: string) {
    const ivw = getInfonlineTag(section, value);

    // @ts-ignore
    window['infonlineCounter'](ivw);
}

export function getInfonlineTag(section: string, value: string) {
    let ivw = value;
    // @ts-ignore
    const _section: any = dictionary[section];
    if(_section && _section[value]) {
        // @ts-ignore
        ivw = _section[value];
    }

    return ivw;
}

